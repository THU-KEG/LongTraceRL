from .base_message_processor import MessageProcessor


class Qwen3MessageProcessor(MessageProcessor):
    """
    Message processor for Qwen3 (Thinking) series models.
    Only supports single-turn conversation, no tool calls.

    Prompt format (generated by tokenizer.apply_chat_template):
        <|im_start|>user\n{user_content}<|im_end|>\n<|im_start|>assistant\n<think>\n

    Full response format:
        {thinking}\n</think>\n\n{content}<|im_end|>

    Special token IDs:
        151643  <|endoftext|>
        151644  <|im_start|>
        151645  <|im_end|>   (eos / stop token)
    """

    IM_START = "<|im_start|>"
    IM_END = "<|im_end|>"
    ENDOFTEXT = "<|endoftext|>"
    THINK_END = "</think>"

    def __init__(self, tokenizer, **kwargs):
        self.tokenizer = tokenizer

    def apply_chat_template(
        self,
        messages: list,
        stop_token: str = None,
        add_special_tokens: bool = True,
    ):
        for message in messages:
            assert "assistant" not in message["role"]
        return self.tokenizer.apply_chat_template(
            messages, tokenize=False, add_generation_prompt=True
        )

    def parse_model_response(self, response: str, truncated: bool = False, aborted: bool = False):
        """
        Parse the raw response generated by sglang.

        Normal response format: "{thinking}\n</think>\n\n{content}<|im_end|>"
        When truncated/aborted, save as-is without format validation.
        """
        raw_response = response
        stop_token = None
        ill_formed_reasons = []
        finish_turn = False

        # truncated / aborted: return as-is
        if truncated:
            return [{"role": "assistant", "truncated_content": raw_response}], stop_token, False, finish_turn
        if aborted:
            return [{"role": "assistant", "aborted_content": raw_response}], stop_token, False, finish_turn

        # strip stop token
        for special_token in [self.IM_END, self.ENDOFTEXT]:
            if response.endswith(special_token):
                stop_token = special_token
                response = response[:-len(stop_token)]
                break
        if not stop_token:
            print(f"NOT FOUND STOP TOKEN: {response}")
            return [{"role": "assistant", "aborted_content": raw_response}], stop_token, False, finish_turn

        # <|endoftext|> as stop token is abnormal
        if stop_token == self.ENDOFTEXT:
            ill_formed_reasons.append(f"Error stop token: {stop_token}")

        # validate thinking format: should have exactly one </think>
        if response.count(self.THINK_END) != 1:
            ill_formed_reasons.append("Wrong thinking format.")

        # extract reasoning_content and content
        reasoning_content = response
        content = ""
        if self.THINK_END in response:
            reasoning_content, content = response.rsplit(self.THINK_END, 1)
            reasoning_content = reasoning_content.removesuffix("\n")
            content = content.removeprefix("\n\n")

        # validate content format
        if not reasoning_content:
            ill_formed_reasons.append("Missing reasoning_content.")
        if not content:
            ill_formed_reasons.append("Missing response content.")
        if content:
            for special_token in [self.IM_START, self.IM_END, self.ENDOFTEXT]:  # LNY: content still has special tokens after stripping stop token, abnormal
                if special_token in content:
                    ill_formed_reasons.append(f"Content contains special token: {special_token}")

        message = {"role": "assistant"}
        if reasoning_content:
            message["reasoning_content"] = reasoning_content
        if content:
            message["content"] = content
        if ill_formed_reasons:
            message["ill_formed_content"] = raw_response
            message["ill_formed_reasons"] = ill_formed_reasons
            print(f"ILL FORMED RESPONSE:\n{[str(raw_response)]}\nILL FORMED REASON:\n{ill_formed_reasons}")

        finish_turn = True
        return [message], stop_token, len(ill_formed_reasons) > 0, finish_turn
